package clinic;

/**
 * Enum representing the approval status of a request or action in the clinic.
 * The possible approval statuses are:
 * PENDING: The request or action is awaiting approval.
 * APPROVED: The request or action has been approved.
 * DENIED: The request or action has been denied.
 */
public enum ApprovalStatus {
  PENDING,
  APPROVED,
  DENIED;
}
