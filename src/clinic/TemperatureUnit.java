package clinic;

/**
 * The {@link TemperatureUnit} enum represents the units of measurement
 * for body temperature readings. It allows the system to support both
 * Celsius and Fahrenheit scales.
 */
public enum TemperatureUnit {
  /**
   * Represents temperature in degrees Celsius.
   */
  CELSIUS,

  /**
   * Represents temperature in degrees Fahrenheit.
   */
  FAHRENHEIT;
}
